#include <stdio.h>
#include <unistd.h>

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*tab;

	tab = (char *)s;
	i = 0;
	while (i <= n)
	{
		tab[i] = (char)'\0';
		i++;
	}
}
